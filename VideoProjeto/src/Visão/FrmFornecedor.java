    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        // TODO add your handling code here:
        
          String[] options = {"Sim", "Não"};

        int resposta = JOptionPane.showOptionDialog(null, "Tem certeza que deseja Salvar os dados ?",
                "Confirmar",JOptionPane.YES_NO_CANCEL_OPTION,
                JOptionPane.QUESTION_MESSAGE, null, options, options[1]);
        

        if (resposta == 1) {
        	
        	return;//Este é o Botao do NAO. Ira retornar para a Calculadora
        }
        
        
       
        
        if(resposta == 0){//Este é o Botao do SIM. Ira sair do seu Programa
         
        
        
        if(muda==1){
        mod.setNome(jTextFieldNome.getText());
        mod.setEndereco(jTextFieldEnd.getText());
        mod.setCNPJ(jFormattedTextFieldCNPJ.getText());
        mod.setBairro((String)jComboBox1.getSelectedItem());   
               control.Salvar(mod);
        
        jTextFieldEnd.setEnabled(!true);
        jTextFieldNome.setEnabled(!true);
        jTextFieldNome.setText("");
        jTextFieldEnd.setText("");
        jTextFieldId.setText(""); 
        jFormattedTextFieldCNPJ.setText("");
        jFormattedTextFieldCNPJ.setEnabled(!true);
        jButtonSalvar.setEnabled(!true);
        jButtonNovo.setEnabled(!false);
        preencherTabela("select * from fornecedores");
        
        }else{
        mod.setNome(jTextFieldNome.getText());
        mod.setEndereco(jTextFieldEnd.getText());
        mod.setCNPJ(jFormattedTextFieldCNPJ.getText());
        mod.setBairro((String)jComboBox1.getSelectedItem());  
        control.Alterar(mod);
        jTextFieldEnd.setEnabled(!true);
        jTextFieldNome.setEnabled(!true);
        jTextFieldNome.setText("");
        jTextFieldEnd.setText("");
        jTextFieldId.setText("");        
        jFormattedTextFieldCNPJ.setEnabled(!true);
        jButtonSalvar.setEnabled(!true);
        jButtonNovo.setEnabled(!false);
        preencherTabela("select * from fornecedores");
                  //JOptionPane.showMessageDialog(rootPane,"Alterar");
        }   
       
        
        
        
      }
       

    
    }//GEN-LAST:event_jButtonSalvarActionPerformed

