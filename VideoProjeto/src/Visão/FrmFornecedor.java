    private void jComboBox1MouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jComboBox1MouseEntered
         try {
            // TODO add your handling code here:
            conn.conexao();
            conn.execultasql("select * from bairro inner join cidade on bairro.id_cidade=cidade.id_cidade inner join estados on cidade.id_estado=estados.id_estado where nome_bairro ='"+jComboBox1.getSelectedItem()+"'");
            conn.rs.first();
            jLabelCidade.setText(conn.rs.getString("nome_cidade"));
            jLabelEstado.setText(conn.rs.getString("sigla_estado"));
        } catch (SQLException ex) {      
            JOptionPane.showMessageDialog(rootPane,"Erro ao selecionar o bairro!\nErro:"+ ex);
        }
    }//GEN-LAST:event_jComboBox1MouseEntered

