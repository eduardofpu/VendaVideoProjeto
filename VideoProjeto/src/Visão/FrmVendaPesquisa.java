    public void preencherTabela(String sql){
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"ID","Data Venda","P.V ","Cliente"};
        conn.conexao();
        conn.execultasql(sql);                
            try {
                conn.rs.first();
                do {
                    //JOptionPane.showMessageDialog(rootPane,conn.rs.getInt(1));
                    dados.add(new Object[]{conn.rs.getInt("id_venda"),conn.rs.getString("data_venda"),conn.rs.getString("valor_venda"),conn.rs.getString("nome_cliente")});
                }while(conn.rs.next());
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane,"Erro ao preencher o ArrayList!\nErro:"+ex);
            }
          ModeloTabela modelo = new ModeloTabela(dados, Colunas);
          Table.setModel(modelo);
          Table.getColumnModel().getColumn(0).setPreferredWidth(50);
          Table.getColumnModel().getColumn(0).setResizable(false);
          Table.getColumnModel().getColumn(1).setPreferredWidth(100);
          Table.getColumnModel().getColumn(1).setResizable(false); 
          Table.getColumnModel().getColumn(2).setPreferredWidth(50);
          Table.getColumnModel().getColumn(2).setResizable(false);
          Table.getColumnModel().getColumn(3).setPreferredWidth(150);
          Table.getColumnModel().getColumn(3).setResizable(false); 
          Table.getTableHeader().setReorderingAllowed(false);
          Table.setAutoResizeMode(Table.AUTO_RESIZE_OFF);
          Table.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);                                   
    }
